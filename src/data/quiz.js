export const LETTER_TO_ARCHETYPE = {
  E: 'expert',
  G: 'grande-gueule',
  L: 'leader',
  X: 'explorateur',
}

export const PROFILE_LOOKUP = {
  'expert-grande-gueule': 'sniper',
  'expert-leader': 'mentor',
  'expert-explorateur': 'chercheur',
  'grande-gueule-expert': 'franc-tireur',
  'grande-gueule-leader': 'tribun',
  'grande-gueule-explorateur': 'disrupteur',
  'leader-expert': 'guide',
  'leader-grande-gueule': 'chef-de-fil',
  'leader-explorateur': 'batisseur',
  'explorateur-expert': 'pionnier',
  'explorateur-grande-gueule': 'rebelle',
  'explorateur-leader': 'eclaireur',
}

export function calculateScores(answers) {
  const scores = { E: 0, G: 0, L: 0, X: 0 }
  answers.forEach((selectedLetters, idx) => {
    selectedLetters.forEach(letter => {
      const answer = QUESTIONS[idx]?.answers.find(a => a.letter === letter)
      if (answer) scores[answer.archetype]++
    })
  })
  return scores
}

// Tie-breaking order: E > G > L > X (defined priority when scores are equal)
const TIEBREAK_ORDER = { E: 0, G: 1, L: 2, X: 3 }

export function getProfileFromScores(scores) {
  const sorted = Object.entries(scores).sort((a, b) => {
    if (b[1] !== a[1]) return b[1] - a[1]           // higher score first
    return TIEBREAK_ORDER[a[0]] - TIEBREAK_ORDER[b[0]] // tie → E > G > L > X
  })

  const majorLetter = sorted[0][0]
  const minorLetter = sorted[1][0]
  const major = LETTER_TO_ARCHETYPE[majorLetter]
  const minor = LETTER_TO_ARCHETYPE[minorLetter]
  const profileId = PROFILE_LOOKUP[`${major}-${minor}`] ?? 'disrupteur' // safe fallback

  return { major, minor, profileId }
}
